// API do Vocabulário
const VocabularyAPI = {
  // Obter todas as categorias
  getCategories() {
    return Object.keys(VOCABULARY_DATA.categories);
  },

  // Obter radicais de uma categoria
  getRadicalsByCategory(category) {
    const cat = VOCABULARY_DATA.categories[category];
    if (!cat) return [];
    return cat.radicals.map(r => VOCABULARY_DATA.radicals[r]).filter(Boolean);
  },

  // Obter um radical específico
  getRadical(radicalChar) {
    return VOCABULARY_DATA.radicals[radicalChar] || null;
  },

  // Obter todos os caracteres
  getAllCharacters() {
    const chars = [];
    Object.values(VOCABULARY_DATA.radicals).forEach(radical => {
      if (radical.characters) {
        chars.push(...radical.characters);
      }
    });
    return chars;
  },

  // Pesquisar vocabulário
  search(query) {
    if (!query) return [];
    const lowerQuery = query.toLowerCase();
    return this.getAllCharacters().filter(char =>
      char.char.includes(query) ||
      char.pinyin.toLowerCase().includes(lowerQuery) ||
      char.meaning.toLowerCase().includes(lowerQuery)
    );
  },

  // Filtrar por nível HSK
  getByHSK(level) {
    return this.getAllCharacters().filter(char => char.hsk === level);
  },

  // Filtrar por tipo (verbo, substantivo, etc)
  getByType(type) {
    return this.getAllCharacters().filter(char => 
      char.type && char.type.includes(type.toLowerCase())
    );
  },

  // Obter estatísticas
  getStats() {
    const chars = this.getAllCharacters();
    return {
      totalCategories: Object.keys(VOCABULARY_DATA.categories).length,
      totalRadicals: Object.keys(VOCABULARY_DATA.radicals).length,
      totalCharacters: chars.length,
      byHSK: {
        1: chars.filter(c => c.hsk === 1).length,
        2: chars.filter(c => c.hsk === 2).length,
        3: chars.filter(c => c.hsk === 3).length,
        4: chars.filter(c => c.hsk === 4).length,
        5: chars.filter(c => c.hsk === 5).length,
        6: chars.filter(c => c.hsk === 6).length
      }
    };
  }
};

// Tornar disponível globalmente
if (typeof window !== 'undefined') {
  window.VocabularyAPI = VocabularyAPI;
  window.VOCABULARY_DATA = VOCABULARY_DATA;
}
